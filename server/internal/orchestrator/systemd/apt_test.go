package systemd_test

import (
	"testing"

	"github.com/stretchr/testify/require"

	"github.com/pgEdge/control-plane/server/internal/ds"
	"github.com/pgEdge/control-plane/server/internal/orchestrator/systemd"
)

func TestApt(t *testing.T) {
	t.Run("installed packages successful", func(t *testing.T) {
		apt := systemd.Apt{
			ExecCommand: systemd.MockExecCommand(t, testAptPackageList, "", nil),
		}
		expected := []*systemd.InstalledPostgres{
			{
				Postgres: &systemd.InstalledPackage{
					PostgresMajor: "16",
					Version:       ds.MustParseVersion("16.13"),
					Name:          "pgedge-postgresql-16",
				},
				Spock: []*systemd.InstalledPackage{
					{
						PostgresMajor: "16",
						Version:       ds.MustParseVersion("5.0.7"),
						Name:          "pgedge-postgresql-16-spock50",
					},
				},
			},
			{
				Postgres: &systemd.InstalledPackage{
					PostgresMajor: "17",
					Version:       ds.MustParseVersion("17.9"),
					Name:          "pgedge-postgresql-17",
				},
				Spock: []*systemd.InstalledPackage{
					{
						PostgresMajor: "17",
						Version:       ds.MustParseVersion("5.0.7"),
						Name:          "pgedge-postgresql-17-spock50",
					},
				},
			},
			{
				Postgres: &systemd.InstalledPackage{
					PostgresMajor: "18",
					Version:       ds.MustParseVersion("18.3"),
					Name:          "pgedge-postgresql-18",
				},
				Spock: []*systemd.InstalledPackage{
					{
						PostgresMajor: "18",
						Version:       ds.MustParseVersion("5.0.7"),
						Name:          "pgedge-postgresql-18-spock50",
					},
				},
			},
		}
		installed, err := apt.InstalledPostgresVersions(t.Context())
		require.NoError(t, err)
		require.Equal(t, expected, installed)
	})
}

